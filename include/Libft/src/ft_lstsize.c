#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	if (!lst)
		return (0);
	count++;
	if (lst->next != NULL)
		count = count + ft_lstsize(lst->next);
	return (count);
}
